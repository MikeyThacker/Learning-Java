import java.security.KeyStore;

public class ConditionalStatements {
    void ifStatement() {
        /*
        IF statement know as single-alternative selection

        if (<condition>) {
            <code to execute if true>
        }
        <code to execute after checking condition>

        Comparisons for primitive data types:
        >, <: Greater than, Smaller than
        >=, <=: Greater/Smaller than or equal to
        ==: Equal to
        !=: Not equal to

        For Classes e.g., strings:
        <string>.equals(<string>): check equality of two strings
         */
    }

    void ifElseStatement() {
        /*
        if (<condition>) {
            <code to execute if true>
        } else {
         <code to execute if false>
        }
         */
    }

    void elseIFStatement() {
        /*
        Better to Use an else-if statement to stop range error and unnecessary indentation

        if (<condition>) {
            <code to execute if true>
        } else if (<condition> {
         <code to execute if next condition is true>
         } else if (<condition> {
         <code to execute if next condition is true>
         } else {
         <code to execute if false>
         }

         You can have as many else-if statements as you want
         */
    }

    void logicalOperators(){
        /*
        &&: AND
        ||: OR
         */
    }
}
